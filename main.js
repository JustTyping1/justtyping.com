function scrolltoabout() {
    var h = window.innerHeight;
    window.scrollTo({
        top: h,
        behavior: 'smooth'
    });
}
function scrolltoproject() {
    var h = window.innerHeight;
    window.scrollTo({
        top: 2*h,
        behavior: 'smooth'
    });
}
function scrolltocontact() {
    var h = window.innerHeight;
    window.scrollTo({
        top: 3*h,
        behavior: 'smooth'
    });
}